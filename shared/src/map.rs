// map.rs
// Defines the Map grid struct and the three level layouts.
// Used by both the server (collision detection) and client (raycasting).

#[derive(Debug, Clone)]
pub struct Map {
    pub width: usize,
    pub height: usize,
    pub cells: Vec<u8>, // 0 = empty, 1 = wall
}

impl Map {
    pub fn is_wall(&self, x: usize, y: usize) -> bool {
        self.cells[y * self.width + x] == 1
    }
}

